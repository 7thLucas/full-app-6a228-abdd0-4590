import type { MapDef, AreaId } from "./types";

// Collision helper: build a grid from ASCII rows.
//   '#' = blocked, '.' = walkable. All rows must share the same width.
function grid(rows: string[]): number[][] {
  return rows.map((row) => row.split("").map((c) => (c === "#" ? 1 : 0)));
}

